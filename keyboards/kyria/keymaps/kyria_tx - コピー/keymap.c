layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state))
    {
    case _LOWER:
        rgblight_mode(4);
        break;
    case _RAISE:
        rgblight_mode(7);
        break;
    case _NUM:
        rgblight_mode(22);
        break;
    case _QWERTY:
        rgblight_mode(10);
        break;
    }
    return state;
}

